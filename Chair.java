public class Chair {
    String color;
    int height;
    int noOfLeg;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getNoOfLeg() {
        return noOfLeg;
    }

    public void setNoOfLeg(int noOfLeg) {
        this.noOfLeg = noOfLeg;
    }
}
